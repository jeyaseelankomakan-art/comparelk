if (!function_exists('getCurrentLang')) {
    function getCurrentLang(): string
    {
        if (session_status() === PHP_SESSION_NONE)
            session_start();
        $lang = $_SESSION['lang'] ?? $_COOKIE['lang'] ?? 'en';
        return in_array($lang, ['en', 'ta', 'si']) ? $lang : 'en';
    }

    function setLang(string $code): void
    {
        if (session_status() === PHP_SESSION_NONE)
            session_start();
        if (in_array($code, ['en', 'ta', 'si'])) {
            $_SESSION['lang'] = $code;
            setcookie('lang', $code, time() + (86400 * 365), '/');
        }
    }

    function t(string $key): string
    {
        global $LANG;
        return $LANG[$key] ?? $key;
    }
}

